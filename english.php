<b>My dream</b>

<p>Deep blue night,<br>
With yellow moon-light.<br>
And a black long tree<br>
Oh! Here I am free!<br>
Here I can do<br>
To what I wish,<br>
Here I can see<br>
The bright copper dish.<br>
Here I can see<br>
With all my eyes-<br>
The moon, the stars,<br>
In the skies.<br>
I think I can fly<br>
In the large sky,<br>
I shall reach the moon<br>
Yes, very soon!<br>
It may be wrong or right,<br>
But it is true that<br>
The sun begins to give light<br>
And drives away my dream and night.</p>
